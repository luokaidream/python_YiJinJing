# 字符串的表示方式，以及用法

# 单引号方式
str1 = 'I am a string type'

# 双引号方式, 注意双引号里可以有单引号，双引号中的单引号并当成正常的字符串
str2 = "Today is a good day, I 'm LiLei"

# 三引号方式，三引号多用于文字较多的字符串
"""
i am line 1
i am line 2
i am line 3
"""

# 字符串，是不可变数据类型，拥有切片功能，且有很多方法

# 切片

a = str1[1:3]
print(a)
